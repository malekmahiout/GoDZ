@Injectable({
  providedIn: 'root'
})
export class TranslationService {
  private currentLanguage = new BehaviorSubject<AppLanguage>('fr');
  readonly currentLanguage$ = this.currentLanguage.asObservable();
  private nameCache: Map<string, string> = new Map();

  constructor() {
    try {
      const saved = localStorage.getItem('godz_name_cache');
      if (saved) this.nameCache = new Map(Object.entries(JSON.parse(saved)));
    } catch {}
  }

  get(key: string): string {
    const lang = this.currentLanguage.getValue();
    return TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS['fr'][key] ?? key;
  }

  setLanguage(lang: AppLanguage): void {
    this.currentLanguage.next(lang);
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
  }

  getCurrentLanguage(): AppLanguage {
    return this.currentLanguage.getValue();
  }

  getAvailableLanguages(): { code: AppLanguage; label: string; flag: string }[] {
    return [
      { code: 'fr', label: 'Français', flag: '🇫🇷' },
      { code: 'ar', label: 'العربية', flag: '🇩🇿' },
      { code: 'en', label: 'English', flag: '🇬🇧' },
      { code: 'es', label: 'Español', flag: '🇪🇸' },
      { code: 'de', label: 'Deutsch', flag: '🇩🇪' }
    ];
  }

  isRtl(): boolean {
    return this.currentLanguage.getValue() === 'ar';
  }

  getPlaceName(place: { id?: string; nameFr: string; nameAr?: string; nameEn?: string }): string {
    const lang = this.getCurrentLanguage();
    if (lang === 'ar' && place.nameAr) return place.nameAr;
    if (lang === 'en' && place.nameEn) return place.nameEn;
    if ((lang === 'es' || lang === 'de') && place.id) {
      const cached = this.nameCache.get(`${place.id}_${lang}`);
      if (cached) return cached;
      return place.nameEn || place.nameFr;
    }
    if ((lang === 'es' || lang === 'de') && place.nameEn) return place.nameEn;
    return place.nameFr;
  }

  setCachedPlaceName(id: string, lang: AppLanguage, name: string): void {
    this.nameCache.set(`${id}_${lang}`, name);
    try {
      const obj: Record<string, string> = {};
      this.nameCache.forEach((v, k) => { obj[k] = v; });
      localStorage.setItem('godz_name_cache', JSON.stringify(obj));
    } catch {}
  }

  hasCachedPlaceName(id: string, lang: AppLanguage): boolean {
    return this.nameCache.has(`${id}_${lang}`);
  }

  getPlaceDescription(place: { description: string; descriptionAr?: string; descriptionEn?: string }): string {
    const lang = this.getCurrentLanguage();
    if (lang === 'ar' && place.descriptionAr) return place.descriptionAr;
    if ((lang === 'en' || lang === 'es' || lang === 'de') && place.descriptionEn) return place.descriptionEn;
    return place.description;
  }

  getWilaya(frName: string): string {
    const lang = this.getCurrentLanguage();
    if (lang === 'ar') return WILAYA_AR[frName] ?? frName;
    if (lang === 'en') return WILAYA_EN[frName] ?? frName;
    return frName;
  }
}
